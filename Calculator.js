//Arrow Function

//Addition
const add=(a,b)=>{
    let result=a+b;
    return result;
}
console.log(add(5,11));

//Subtraction
const Subtraction=(a,b)=>{
    let result=a-b;
    return result;
}
console.log(Subtraction(10,5));

//Multiplication
const Multiplication=(a,b)=>{
    let result=a*b;
    return result;
}
console.log(Multiplication(5,11));

//Division
const Division=(a,b)=>{
    let result=a/b;
    return result;
}
console.log(Division(10,2));

//Modulus
const Modulus=(a,b)=>{
    let result=a%b;
    return result;
}
console.log(Modulus(25,5));



